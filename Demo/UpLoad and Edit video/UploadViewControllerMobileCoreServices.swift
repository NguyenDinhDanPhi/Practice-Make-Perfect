import UIKit
import MobileCoreServices
import AVFoundation
import Photos

class UploadViewControllerMobileCoreServices: UIViewController,
                                             UIImagePickerControllerDelegate,
                                             UINavigationControllerDelegate,
                                             UIVideoEditorControllerDelegate {
    
    // MARK: - UI Components
    private let selectButton: UIButton = {
        let btn = UIButton(type: .system)
        btn.setTitle("Ch·ªçn video ƒë·ªÉ t·∫£i l√™n", for: .normal)
        btn.translatesAutoresizingMaskIntoConstraints = false
        btn.titleLabel?.font = .systemFont(ofSize: 18, weight: .medium)
        btn.setTitleColor(.white, for: .normal)
        btn.backgroundColor = .systemBlue
        btn.layer.cornerRadius = 8
        return btn
    }()
    
    private let listButton: UIButton = {
        let btn = UIButton(type: .system)
        btn.setTitle("Xem danh s√°ch video", for: .normal)
        btn.translatesAutoresizingMaskIntoConstraints = false
        btn.titleLabel?.font = .systemFont(ofSize: 18, weight: .medium)
        btn.setTitleColor(.white, for: .normal)
        btn.backgroundColor = .systemGreen
        btn.layer.cornerRadius = 8
        return btn
    }()
    
    private let activityIndicator: UIActivityIndicatorView = {
        let indicator = UIActivityIndicatorView(style: .large)
        indicator.translatesAutoresizingMaskIntoConstraints = false
        indicator.hidesWhenStopped = true
        return indicator
    }()
    
    // MARK: - Properties
    private var tempVideoURL: URL?
    
    // MARK: - Lifecycle
    override func viewDidLoad() {
        super.viewDidLoad()
        title = "Upload Video"
        view.backgroundColor = .systemBackground
        setupLayout()
        setupActions()
    }
    
    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)
        print("üì± UploadViewController viewWillAppear")
    }
    
    // MARK: - Setup
    private func setupLayout() {
        view.addSubview(selectButton)
        view.addSubview(listButton)
        view.addSubview(activityIndicator)
        
        NSLayoutConstraint.activate([
            selectButton.centerXAnchor.constraint(equalTo: view.centerXAnchor),
            selectButton.centerYAnchor.constraint(equalTo: view.centerYAnchor, constant: -40),
            selectButton.widthAnchor.constraint(equalToConstant: 200),
            selectButton.heightAnchor.constraint(equalToConstant: 50),
            
            listButton.centerXAnchor.constraint(equalTo: view.centerXAnchor),
            listButton.topAnchor.constraint(equalTo: selectButton.bottomAnchor, constant: 20),
            listButton.widthAnchor.constraint(equalToConstant: 200),
            listButton.heightAnchor.constraint(equalToConstant: 50),
            
            activityIndicator.centerXAnchor.constraint(equalTo: view.centerXAnchor),
            activityIndicator.centerYAnchor.constraint(equalTo: view.centerYAnchor)
        ])
    }
    
    private func setupActions() {
        selectButton.addTarget(self, action: #selector(selectVideo), for: .touchUpInside)
        listButton.addTarget(self, action: #selector(showList), for: .touchUpInside)
    }
    
    // MARK: - Actions
    @objc private func selectVideo() {
        print("üé¨ B·∫Øt ƒë·∫ßu ch·ªçn video")
        
        // Ki·ªÉm tra quy·ªÅn truy c·∫≠p Photos
        PHPhotoLibrary.requestAuthorization { [weak self] status in
            DispatchQueue.main.async {
                switch status {
                case .authorized, .limited:
                    self?.presentVideoPicker()
                case .denied, .restricted:
                    self?.showAlert(title: "Kh√¥ng c√≥ quy·ªÅn",
                                   message: "Vui l√≤ng c·∫•p quy·ªÅn truy c·∫≠p Photos trong Settings")
                case .notDetermined:
                    break
                @unknown default:
                    break
                }
            }
        }
    }
    
    private func presentVideoPicker() {
        let picker = UIImagePickerController()
        picker.sourceType = .photoLibrary
        picker.mediaTypes = [kUTTypeMovie as String]
        picker.videoQuality = .typeHigh
        picker.delegate = self
        picker.modalPresentationStyle = .fullScreen
        picker.allowsEditing = false // T·∫Øt editing c·ªßa picker, d√πng UIVideoEditorController
        
        print("üìπ Presenting video picker")
        present(picker, animated: true)
    }
    
    @objc private func showList() {
        print("üìã Chuy·ªÉn ƒë·∫øn danh s√°ch video")
        let listVC = VideoListViewController()
        navigationController?.pushViewController(listVC, animated: true)
    }
    
    // MARK: - UIImagePickerControllerDelegate
    func imagePickerController(_ picker: UIImagePickerController,
                              didFinishPickingMediaWithInfo info: [UIImagePickerController.InfoKey: Any]) {
        print("‚úÖ ƒê√£ ch·ªçn video t·ª´ picker")
        
        picker.dismiss(animated: true)
        
        guard let mediaURL = info[.mediaURL] as? URL else {
            print("‚ùå Kh√¥ng t√¨m th·∫•y mediaURL")
            showAlert(title: "L·ªói", message: "Kh√¥ng th·ªÉ l·∫•y video")
            return
        }
        
        print("üìç Video URL:", mediaURL.path)
        print("üìä File exists:", FileManager.default.fileExists(atPath: mediaURL.path))
        
        // L∆∞u temp URL ƒë·ªÉ d√πng sau
        self.tempVideoURL = mediaURL
        
        // Ki·ªÉm tra xem c√≥ th·ªÉ edit kh√¥ng
        if UIVideoEditorController.canEditVideo(atPath: mediaURL.path) {
            print("‚úÇÔ∏è Video c√≥ th·ªÉ edit, presenting editor...")
            presentVideoEditor(with: mediaURL)
        } else {
            print("‚ö†Ô∏è Video kh√¥ng th·ªÉ edit, l∆∞u tr·ª±c ti·∫øp")
            saveVideoDirectly(from: mediaURL)
        }
    }
    
    func imagePickerControllerDidCancel(_ picker: UIImagePickerController) {
        print("‚ùå User cancelled picker")
        picker.dismiss(animated: true)
    }
    
    // MARK: - Video Editor
    private func presentVideoEditor(with url: URL) {
        let editor = UIVideoEditorController()
        editor.videoPath = url.path
        editor.videoMaximumDuration = 600 // 10 ph√∫t
        editor.videoQuality = .typeHigh
        editor.delegate = self
        editor.modalPresentationStyle = .fullScreen
        
        print("üé¨ Presenting video editor v·ªõi path:", url.path)
        present(editor, animated: true)
    }
    
    // MARK: - UIVideoEditorControllerDelegate
    func videoEditorController(_ editor: UIVideoEditorController,
                              didSaveEditedVideoToPath editedVideoPath: String) {
        print("‚úÖ videoEditorController:didSaveEditedVideoToPath: ƒê∆Ø·ª¢C G·ªåI!")
        print("üìç Edited video path:", editedVideoPath)
        
        let editedURL = URL(fileURLWithPath: editedVideoPath)
        
        // L∆∞u video ƒë√£ edit
        showLoading(true)
        
        DispatchQueue.global(qos: .userInitiated).async { [weak self] in
            do {
                try VideoManager.shared.saveVideo(at: editedURL)
                print("‚úÖ ƒê√£ l∆∞u video v√†o Documents")
                
                DispatchQueue.main.async {
                    self?.showLoading(false)
                    editor.dismiss(animated: true) {
                        self?.showAlert(title: "Th√†nh c√¥ng",
                                       message: "Video ƒë√£ ƒë∆∞·ª£c l∆∞u sau khi ch·ªânh s·ª≠a")
                        self?.cleanupTempVideo()
                    }
                }
            } catch {
                print("‚ùå L·ªói l∆∞u video:", error)
                DispatchQueue.main.async {
                    self?.showLoading(false)
                    editor.dismiss(animated: true) {
                        self?.showAlert(title: "L·ªói",
                                       message: "Kh√¥ng th·ªÉ l∆∞u video: \(error.localizedDescription)")
                    }
                }
            }
        }
    }
    
    func videoEditorControllerDidCancel(_ editor: UIVideoEditorController) {
        print("‚ùå User cancelled editor")
        editor.dismiss(animated: true) { [weak self] in
            self?.cleanupTempVideo()
        }
    }
    
    func videoEditorController(_ editor: UIVideoEditorController,
                              didFailWithError error: Error) {
        print("‚ùå Video editor failed v·ªõi error:", error)
        editor.dismiss(animated: true) { [weak self] in
            self?.showAlert(title: "L·ªói ch·ªânh s·ª≠a",
                           message: error.localizedDescription)
            self?.cleanupTempVideo()
        }
    }
    
    // MARK: - Helper Methods
    private func saveVideoDirectly(from url: URL) {
        showLoading(true)
        
        DispatchQueue.global(qos: .userInitiated).async { [weak self] in
            do {
                try VideoManager.shared.saveVideo(at: url)
                print("‚úÖ ƒê√£ l∆∞u video tr·ª±c ti·∫øp")
                
                DispatchQueue.main.async {
                    self?.showLoading(false)
                    self?.showAlert(title: "Th√†nh c√¥ng",
                                   message: "Video ƒë√£ ƒë∆∞·ª£c l∆∞u (kh√¥ng c·∫ßn ch·ªânh s·ª≠a)")
                    self?.cleanupTempVideo()
                }
            } catch {
                print("‚ùå L·ªói l∆∞u video tr·ª±c ti·∫øp:", error)
                DispatchQueue.main.async {
                    self?.showLoading(false)
                    self?.showAlert(title: "L·ªói",
                                   message: "Kh√¥ng th·ªÉ l∆∞u video: \(error.localizedDescription)")
                }
            }
        }
    }
    
    private func cleanupTempVideo() {
        // Cleanup temp video n·∫øu c·∫ßn
        if let tempURL = tempVideoURL {
            print("üßπ Cleaning up temp video")
            try? FileManager.default.removeItem(at: tempURL)
            tempVideoURL = nil
        }
    }
    
    private func showLoading(_ show: Bool) {
        if show {
            activityIndicator.startAnimating()
            selectButton.isEnabled = false
            listButton.isEnabled = false
        } else {
            activityIndicator.stopAnimating()
            selectButton.isEnabled = true
            listButton.isEnabled = true
        }
    }
    
    private func showAlert(title: String, message: String, completion: (() -> Void)? = nil) {
        let alert = UIAlertController(title: title,
                                     message: message,
                                     preferredStyle: .alert)
        alert.addAction(UIAlertAction(title: "OK", style: .default) { _ in
            completion?()
        })
        present(alert, animated: true)
    }
}

// MARK: - Debug Extension
extension UploadViewControllerMobileCoreServices {
    override var debugDescription: String {
        return """
        UploadViewController Debug Info:
        - Has temp video: \(tempVideoURL != nil)
        - Is loading: \(activityIndicator.isAnimating)
        """
    }
}
